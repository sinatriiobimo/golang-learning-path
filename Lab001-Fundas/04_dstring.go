//!! Function pada string
//?? len(“string”)		: Menghitung jumlah karakter di String
//?? “string”[number]	: Mengambil karakter pada posisi yang ditentukan

package main

import "fmt"

func main() {
	fmt.Println("Sinatrio")
	fmt.Println("Sinatrio Bimo")
	fmt.Println("Sinatrio Bimo Wahyudi")

	fmt.Println(len("Sinatrio"))
	fmt.Println("Sinatrio Bimo"[0])
	fmt.Println("Sinatrio Bimo Wahyudi"[1])
}
